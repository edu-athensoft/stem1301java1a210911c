package com.athensoft.day07;

public class DemoCharFromString {

	public static void main(String[] args) {
		String s1 = "ABC";
		
		//get the first char of the string s1
		
		char c1 = s1.charAt(0);
		System.out.println(c1);
		
		char c2 = s1.charAt(1);
		System.out.println(c2);
		
		char c3 = s1.charAt(2);
		System.out.println(c3);
		
	}

}
