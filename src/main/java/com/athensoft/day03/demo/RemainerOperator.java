package com.athensoft.day03.demo;

public class RemainerOperator {

	public static void main(String[] args) {
		//a>0, b>0
		int a = 2;
		int b = 3;
		System.out.println(a+"%"+b+"="+a%b+"\n");
		
		//cd<0
		int c = -2;
		int d = 3;
		System.out.println(c+"%"+d+"="+c%d+"\n");
		
		//e<0, f<0
		int e = -2;
		int f = -3;
		System.out.println(e+"%"+f+"="+e%f+"\n");
		
	}	
}
